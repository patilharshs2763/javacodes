/*
Steps to create a package in java program 
1) Create a Folder on your disk named as "package" and save it. 
2) Create a simple java file inside the package folder/directory folder
3) Inside the created java file create any random package using the package keyword e.g. "package mypkg". 
4) Save the java file with the same name given to the public class/main class. 
5) Finally your created package can be used in your programs as we do in regular java programs.
*/

