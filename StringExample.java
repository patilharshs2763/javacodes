public class StringExample
{
public static void main(String args[])
{
	String s1="Harsh Patil"; //creating string by java string literal
	String s2=new String("Harsh..."); //creating java string by new keyword 
	char chars[]={'H','A','R','H','S','H'};
	String s3=new String(chars); //converting char array to string 
	System.out.println(s1);
	System.out.println(s2);
	System.out.println(s3);
	
}
}
